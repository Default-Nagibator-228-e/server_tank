package battles.tanks.weapons;

public class WeaponWeakeningData {
   public double maximumDamageRadius;
   public double minimumDamagePercent;
   public double minimumDamageRadius;

   public WeaponWeakeningData(double maximumDamageRadius, double minimumDamagePercent, double minimumDamageRadius) {
      this.maximumDamageRadius = maximumDamageRadius;
      this.minimumDamageRadius = minimumDamageRadius;
      this.minimumDamagePercent = minimumDamagePercent;
   }
}
